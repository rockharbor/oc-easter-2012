			<h1>What to Expect</h1>
			<h2 class="orange">Easter at the OC Fairgrounds is a celebration of the freedom offered through the death and resurrection of Jesus Christ.</h2>
		</header>
		<p>The story of Easter will be told through a blend of dance, music, and teaching. Freedom and new life in Jesus will be celebrated through baptism, where a person is submerged and lifted out of water. This act is both a declarative statement to choose to follow Jesus, as well as a symbolic passage into the new life He offers.</p>
		<p>Francis Chan, author of <em>Crazy Love: Overwhelmed by a Relentless God and Forgotten God: Reversing Our Tragic Neglect of the Holy Spirit</em>, will teach a message from the Bible. Francis founded Cornerstorne Community Church in Simi Valley and was the Senior Pastor there for 16 years. For more information on him and his ministry, <a href="http://francischan.org" target="_blank">check out his website</a>.</p>
